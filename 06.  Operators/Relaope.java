import java.util.*;

public class Relaope {
    public static void main(String args[]) {
  

      Scanner sc= new Scanner(System.in);
   // Relational Operator ||
        /*
         * ==
         * !=
         * >
         * <
         * >=
         * <=
         * 
         */
      
        int m = sc.nextInt();

        int n = sc.nextInt();
          
        System.out.println(m == n);
        System.out.println(m != n);
        System.out.println(m > n);
        System.out.println(m < n);
        System.out.println(m >= n);
        System.out.println(m <= n);


         sc.close();
    }
}
