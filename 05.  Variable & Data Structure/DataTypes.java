public class DataTypes {
    public static void main(String args[]) {
  
     // Data Types in java  
     
     
//1. Byte 
byte byt =8;
System.out.print("Byte is - ");
System.out.println(byt);


//2. Character
char cha='A';
System.out.print("Character is - ");
System.out.println(cha);


//3. boolean
boolean var = true;
boolean varc = false;    
System.out.println(var);
System.out.println(varc);

//4. Int 
int price = 10;
System.out.print("Price is - ");
System.out.println(price);

//5. Float
float pric =2.2f;
System.out.print("Price is - ");
System.out.println(pric);

//6. long
long lg=2000000000;
System.out.println(lg);



//7. Double
double dou= 1000000000000.225;
System.out.println(dou);


//8. Short
short sh=200;
System.out.println(sh);

    }
}
